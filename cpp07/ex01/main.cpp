#include "iter.hpp"

int add(int n)
{
    return n + 1;
}

int main()
{
    int arr[] = {1, 2, 3};
    iter(arr, 3, add);
    for (int i = 0; i < 3; i++)
        std::cout << arr[i] << std::endl;
}